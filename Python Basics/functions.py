def summation(a, b):
    return a + b

def multiplication(a, b):
    return a * b

# Directly calling the functions
x = 5
y = 3

sum_result = summation(x, y)
product_result = multiplication(x, y)

print(f"Summation of {x} and {y} is: {sum_result}")
print(f"Multiplication of {x} and {y} is: {product_result}")
